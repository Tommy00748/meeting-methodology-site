function toggleCard(card){
  const isOpen=card.classList.contains('open');
  document.querySelectorAll('.layer-card.open').forEach(c=>{if(c!==card)c.classList.remove('open')});
  card.classList.toggle('open',!isOpen);
}
function toggleQuote(btn){
  const box=btn.nextElementSibling;
  const isOpen=box.classList.contains('show');
  box.classList.toggle('show',!isOpen);
  btn.classList.toggle('open',!isOpen);
  btn.innerHTML=isOpen?'<span>\u25C6</span> 展开金句':'<span>\u25C6</span> 收起金句';
}
